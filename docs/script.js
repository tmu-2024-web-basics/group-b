function closeAllModal() {
    for (let i = 0; i < allModal.length; i++) {
        allModal[i].style.display = "none";
    }
}

function showLoading() {
    document.getElementById('loading').style.display = 'block';
}

function hideLoading() {
    document.getElementById('loading').style.display = 'none';
}

for (let i = 0; i < btns.length; i++) {
    btns[i].onclick = function() {
        closeAllModal();

        let modalId = this.getAttribute("data-modal");
        let modal = document.getElementById(modalId);

        if(modalId=="q0"){

            document.getElementById('startFC').style.display = 'none';
            document.getElementById('topTitle').style.display = 'none';
            document.getElementById('nori-1-icon').style.display = 'none';
            document.getElementById('edamame-1-icon').style.display = 'none';
            document.getElementById('goma-1-icon').style.display = 'none';
            document.getElementById('nitamago-1-icon').style.display = 'none';
            document.getElementById('shiso-1-icon').style.display = 'none';
            document.getElementById('sake-1-icon').style.display = 'none';
            document.getElementById('ume-1-icon').style.display = 'none';
            document.getElementById('yaki-1-icon').style.display = 'none';
            
            
            showLoading();
            setTimeout(() => {
                hideLoading();
                modal.style.display = "block";
                
            }, 2000);
        }else if(modalId=="result1" || modalId=="result2" || modalId=="result3" 
        || modalId=="result4" || modalId=="result5" || modalId=="result6" || modalId=="result7" 
        || modalId=="result8" || modalId=="result9" || modalId=="result10" || modalId=="result11" 
        || modalId=="result12" || modalId=="result13" || modalId=="result14" || modalId=="result15"){
            showLoading();
            setTimeout(() => {
                hideLoading();
                modal.style.display = "block";
                
            }, 2000);
            
        }else{modal.style.display = "block";}
    }
}


    